def aumentar(n):
    valor = n + (n*.1)
    print(f'Aumentando 10% do valor {n}, se torna R$ {valor}')


def diminuir(n):
    valor = n - (n*.1)
    print(f'Diminuindo 10% do valor {n}, se torna R$ {valor}')


def dobro(n):
    valor = n * 2
    print(f'O dobro do valor {n} será R$ {valor}')


def metade(n):
    valor = n/2
    print(f'A metade do valor {n} será R$ {valor}')

